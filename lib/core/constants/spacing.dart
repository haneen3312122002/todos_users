import 'package:flutter_screenutil/flutter_screenutil.dart';

class AppSpacing {
  //  Very small space ( icon & text )
  static final double spaceXS = 4.h;

  // Small space (small widgets, label & field)
  static final double spaceSM = 8.h;

  //  Medium space ( text & button, inside cards)
  static final double spaceMD = 16.h;

  //  Large space (cards or list items)
  static final double spaceLG = 24.h;

  //  Extra large space (e.g., between main sections)
  static final double spaceXL = 32.h;

  //  Huge space (e.g., large separation between screen blocks)
  static final double spaceXXL = 48.h;

  //  Screen horizontal padding (standard left/right margin)
  static final double screenHorizontal = 5.w;

  //  Screen vertical padding (standard top/bottom margin)
  static final double screenVertical = 5.h;

  //  Section padding (internal padding inside a card or container)
  static final double sectionPadding = 16.h;

  // ↕ Space between app sections (title → content)
  static final double sectionSpacing = 28.h;

  //  Space between button & text below it
  static final double buttonTextGap = 12.h;

  //  Space between field title (label) & input field
  static final double labelFieldGap = 6.h;
}
